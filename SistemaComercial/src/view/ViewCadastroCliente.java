    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome;
        String cpf;
        String email;
        Date dataNascimento;
        boolean resultado = true;

        if (txtNome.getText().trim().isEmpty()
                || txtNome.getText().length() > TAM_MAX_NOME) {
            JOptionPane.showMessageDialog(this, "Nome invÃ¡lido!");
        } else {
            nome = txtNome.getText();
            if (txtCpf.getText().trim().isEmpty()
                    || txtCpf.getText().length() > TAM_MAX_CPF) {
                JOptionPane.showMessageDialog(this, "CPF invÃ¡lido!");
            } else {
                cpf = txtCpf.getText();
                if (txtNascimento.getDate()==null) {
                    JOptionPane.showMessageDialog(this, "Data de Nascimento invÃ¡lida!");
                } else {
                    dataNascimento = txtNascimento.getDate();
                    if (txtEmail.getText().trim().isEmpty()
                            || txtEmail.getText().length() > TAM_MAX_EMAIL) {
                        JOptionPane.showMessageDialog(this, "E-Mail invÃ¡lido!");
                    } else {
                        email = txtEmail.getText();
                        if(idCliente==null){
                            resultado = ClienteController.getInstance().createCliente(nome, cpf, email, dataNascimento);

                            if (resultado == true) {
                                JOptionPane.showMessageDialog(this, "Falha ao salvar o cliente!");
                            } else {
                                JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
                                txtNome.setText("");
                                txtCpf.setText("");
                                txtNascimento.setDate(new Date());
                                txtEmail.setText("");
                            }                         
                        }else{
                            resultado = ClienteController.getInstance().updateClienteById(idCliente, nome, cpf, dataNascimento, email);
                            if(resultado==false){
                                JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!");
                                this.setVisible(false);
                            }else{
                                JOptionPane.showMessageDialog(this, "Falha ao atualizar o cliente!");
                            }
                        }

                    }
                }
            }
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

