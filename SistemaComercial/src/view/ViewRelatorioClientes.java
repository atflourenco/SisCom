    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        
        Long idSelecionado;
        boolean resultado;
        int opcao;
        
        if(tblClientes.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Nenhum cliente selecionado!");
        }else{
            opcao = JOptionPane.showConfirmDialog(this, "Deseja deletar?");
            
            if(opcao==JOptionPane.YES_OPTION){
                idSelecionado = (Long) tblClientes.getValueAt(tblClientes.getSelectedRow(), 0);
                
                resultado = ClienteController.getInstance().deleteClienteById(idSelecionado);
                
                if(resultado==false){
                    JOptionPane.showMessageDialog(this, "Cliente deletado com sucesso!");
                    this.carregarDadosDaTabela();
                }else{
                    JOptionPane.showMessageDialog(this, "Falha ao deletar cliente!");
                }
            }else{
                tblClientes.clearSelection();
            }
        }
        
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnDeletarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarTodosActionPerformed
        int opcao;
        boolean resultado;
        if(tblClientes.getRowCount()>0){
            opcao = JOptionPane.showConfirmDialog(this, "Deseja deletar todos?");
            if(opcao==JOptionPane.YES_OPTION){
                resultado = ClienteController.getInstance().deleteAllClientes();
                if(resultado==false){
                    JOptionPane.showMessageDialog(this, "Todos os clientes foram deletados com sucesso!");
                    this.carregarDadosDaTabela();
                }else{
                    JOptionPane.showMessageDialog(this, "Falha ao deletar todos os clientes!");
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Não existem clientes cadastrados!");
        }

    }//GEN-LAST:event_btnDeletarTodosActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Long idSelecionado;
        ViewCadastroCliente vcCliente;

        if(tblClientes.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Nenhum cliente selecionado!");
        }else{
            idSelecionado = (Long) tblClientes.getValueAt(tblClientes.getSelectedRow(),0);
            vcCliente = new ViewCadastroCliente((Frame) this.getParent(), true, idSelecionado);
            vcCliente.setLocationRelativeTo(this);
            vcCliente.setVisible(true);
            this.carregarDadosDaTabela();
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void txtFiltroKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyPressed
        TableRowSorter rs = (TableRowSorter) tblClientes.getRowSorter();
        rs.setRowFilter(RowFilter.regexFilter("(?i)"+ txtFiltro.getText(),0,1));
    }//GEN-LAST:event_txtFiltroKeyPressed

