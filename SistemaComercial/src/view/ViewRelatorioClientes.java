    private void carregarDadosDaTabela(){
        DefaultTableModel modeloTabela = (DefaultTableModel) tblClientes.getModel();
        
        while(modeloTabela.getRowCount()>0){
            modeloTabela.removeRow(0);
        }
        
        List<Cliente> listaClientes = ClienteController.getInstance().readAllClientes();
        
        for(int i=0;i<listaClientes.size();i++){
            Cliente c = listaClientes.get(i);
            Object linha[] = {c.getId(),c.getNome(),c.getCpf()};
            modeloTabela.addRow(linha);
        }        
        tblClientes.getColumnModel().getColumn(0).setPreferredWidth(10);
        tblClientes.getColumnModel().getColumn(1).setPreferredWidth(300);
        tblClientes.getColumnModel().getColumn(2).setPreferredWidth(100);
     
    }
    
